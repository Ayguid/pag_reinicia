                            <div class="header-title" data-toggle="collapse" data-target="#questionThree" aria-expanded="false" aria-controls="questionThree">
                              <i class="lni-plus"></i>  ¿Qué son los trastornos neurológicos?
                            </div>
                          </div>
                          <div id="questionThree" class="collapse" aria-labelledby="headingThree" data-parent="#question">
                            <div class="card-body">
                              <p>
                                Los trastornos neurológicos son aquellas patologías localizados en cualquier punto del sistema nervioso, es decir, anomalías estructurales, bioquímicas o eléctricas  en el cerebro, médula espinal u otros nervios , alterando su correcto funcionamiento., dando lugar a una serie de síntomas.
                              </p>
                              <p class="mt-2">
                                Las causas dependen del trastorno, pudiendo ser incluso aún desconocidas en la actualidad.
                              </p>
                              <p class="mt-2">
                                Los síntomas de estos trastornos pueden ser muy variados, pudiendo producir tanto un déficit como un exceso de actividad neuronal en cualquier sistema del organismo.
                              </p>
                              <p class="mt-2">
                                Ejemplos de los síntomas incluyen parálisis, debilidad muscular, falta de coordinación, pérdida de sensibilidad, convulsiones, confusión, dolor y niveles alterados de consciencia.</p>
