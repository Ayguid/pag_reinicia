                                <i class="lni-plus"></i> ¿Qué es la parálisis cerebral?
                              </div>
                            </div>
                            <div id="questionFour" class="collapse" aria-labelledby="headingThree" data-parent="#question">
                              <div class="card-body">
                                <p>
                                  La parálisis cerebral (PC) es un término amplio que describe  a un grupo de trastornos neurológicos (cerebrales) no progresivos.
                                </p>
                                <p class="mt-2">
                                  Ocurre cuando las áreas del cerebro que controlan el movimiento y la postura no se desarrollan correctamente o se lesionan, afectando los movimientos corporales y la coordinación de los músculos.
                                </p>
                                <p class="mt-2">
                                  Los síntomas de parálisis cerebral difieren en el tipo y la gravedad de una persona a otra, y hasta pueden cambiar en un individuo con el tiempo. Algunas personas con parálisis cerebral también tienen otros trastornos médicos, como retraso mental, convulsiones, visión o audición deteriorada, y sensaciones o percepciones físicas anormales.</p>
                                </div>
                              </div>
                            </div>
