                                </div>
                              </div>
                              <div id="questionOne2" class="collapse" aria-labelledby="headingOne" data-parent="#question">
                                <div class="card-body">
                                  <p>
                                    La parálisis cerebral no es una enfermedad. No es contagiosa y no puede pasarse de una generación a la otra. No hay cura para la parálisis cerebral, pero los tratamientos de apoyo, ciertos medicamentos y en ocasiones la cirugía, pueden ayudar a muchos individuos a mejorar sus habilidades motoras y la capacidad de comunicarse con el mundo.
                                  </p>
                                  <p class="mt-2">
                                    Muchas personas con PC están en tratamiento durante gran parte de su vida, logrando superar dificultades a través de una adecuada atención terapéutica integral , y el uso de numerosos dispositivos de asistencia que se han diseñado para personas con PC y otras enfermedades que provocan discapacidades , mejorando su calidad de vida.
                                  </p>
                                  <p class="mt-2">
                                    Algunos ejemplos son los dispositivos de apoyo ortopédicos, las sillas especiales, los andadores, computadoras, software, sintetizadores de voz y libros con imágenes para ayudar con la comunicación, entre muchos otros.
                                  </p>
